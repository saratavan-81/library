این فعال سیاسی اصلاح طلب طی یادداشتی در کانال تلگرامی خود که آنرا «دومین نامه به دکتر حجاریان درباره اصلاحات ساختاری» نام گذاری کرده بود، روحانیت را متهم به دخالت در اداره حکومت کرده و مدعی شده بود روحانیت نه برای آن ایجاد شده بود و نه آمادگی ایفای چنین نقشی را داشت.

صحیفه امام(ره) در رابطه با نقش نظارتی یا حکومتی روحانیون در نظام جمهوری به وجود نظر نظارتی در اوایل انقلاب اشاره کرده است، اما ولی فقیه بعدا به این نتیجه رسید که حضور مسئولیتی روحانیت بلااشکال است.

تاجزاده در ادامه یادداشت خود مدعی شده است که رهبری در حالی در امور مختلف کشورداری دخالت دارد که هیچ مسئولیتی را متوجه خود نمی داند!

به بنابراین گزارش، با نگاه به امورات و اجرائیات در دولت های گذشته، پر واضح است که رهبر انقلاب همواره از دولت ها حمایت کرده و به نوعی تصمیمات دولت ها را تمکین کرده است. در دولت های اصلاحات، بهار و تدبیر که هرکدام در جهات مختلف، سیاست های خود را دنبال می‌کردند، که رهبری جز حمایت از دولت ها هیچ نقشی در امورات و اجرائیات کشور نداشته است. نکته قابل تامل این است که علیرغم این واقعیتِ قابل لمس، آقای تاجزاده چگونه مدعی دخالت رهبری در امورات کشور شده است؟!

تاجزاده در این بیانیه همچنین تصریح می‌کند که قصد براندازی ندارد و بر حذف کامل ولایت فقیه از قانون اساسی هم پافشاری نمی‌کند!

اما پیش ازین محمد عطریانفر از فعالان سیاسی اصلاح‌طلب در مصاحبه ای پیرامون بحث اصلاحات ساختاری تلویحا تاکید کرده بود که مزاج اظهارات تاجزاده چیزی جز براندازی نیست!

