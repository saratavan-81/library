بنابراین، تعداد کاربران فعال این پیام‌رسان‌ها نه‌تنها نسبت به کل جمعیت استفاده‌کننده از موبایل در ایران بسیار اندک بوده، بلکه در مقایسه با تعداد کاربران پیام‌رسان‌های خارجی نیز بسیار کم است. با در نظر داشتن این موضوع و عدم ارائه آمار و ارقام جدید از تعداد کاربران پیام‌رسان‌های بومی، مشخص نیست این استقبال چه میزان بوده و درخواست راه‌اندازی مرورگرهای بومی تا چه اندازه به گسترش خدمات دیجیتالی داخلی مرتبط است.









               
