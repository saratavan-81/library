<h6>
طراح ترور شهید حاج قاسم سلیمانی کیست ؟ + جزئیات و عکس
</h6>
<br>
«اریک پرینس» نظامی سابق آمریکایی و بنیانگذار شرکت نظامی خصوصی «بلک‌واتر» (که امروزه «آکادمی» نامیده می‌شود)، یکی از بازیگران برجسته در شکل‌گیری سیاست‌های خصمانه‌ی دولت آمریکا در قبال ایران و به طور خاص، ترور شهید سلیمانی است. در گزارش پیش رو بیش‌تر با پرینس آشنا خواهید شد.

اغلب شهرت «اریک پرینس» به خاطر تأسیس شرکت نظامی خصوصی «بلک‌واتر» است که اکنون با تغییر نام به «آکادمی»، بزرگ‌ترین ارتش خصوصی جهان است. این شرکت مخوف جنایت‌های کلانی در سرتاسر دنیا انجام داده و مسئولیت مستقیم آن‌ها به عهده‌ی اریک پرینس، بنیانگذار و مدیرعامل سابق آن، است.

این عملیات‌ها در سودان، یمن، عراق، لیبی، افغانستان و دیگر نقاط دنیا انجام شده است. گزارش‌ها حاکی از آن است در دوره‌ی ریاست پرینس فقط ۱/۶ میلیارد دلار بودجه از سوی دولت آمریکا در قالب قراردادهای غیرمحرمانه به حساب این شرکت واریز شده است.

درباره قراردادهای محرمانه، اطلاعاتی در دست نیست، اما بخشی از این قراردادها توسط آژانس مرکزی اطلاعات آمریکا، سی‌آی‌ای، منعقد شده است.

اریک پرینس، سال ۲۰۱۰، و بعد از رسوایی‌های فراوان، بلک‌واتر را ترک کرد، اما پس از جدایی باز هم اقدامات و علایق نظامی او فروکش نکرد. تنها در یک مورد، سال ۲۰۱۹، سازمان ملل گزارش مفصلی درباره‌ی اقدامات نظامی او در کشور لیبی برای ترور، کودتا یا نقض احکام بین‌المللی منتشر کرد.

در این گزارش فهرست بلندی از اهداف ترور، ارسال تسلیحات غیرقانونی به لیبی و انتقال کاربری هواپیماهای غیرنظامی به نظامی به قیمت صوری ۱۰ دلار ارائه، و گفته شده ارزش این عملیات حداقل ۸۰ میلیون دلار بوده است. البته این تنها مورد اتهام او در سازمان ملل نیست.

پیش‌تر نیز در سال ۲۰۱۱ تحرکات نظامی پرینس در سومالی و اریتره مورد توجه سازمان ملل قرار گفت، ولی به تحریم یا توبیخ او منجر نشد.

اریک پرینس طی سال‌های ۲۰۱۵ و ۲۰۱۶ حامی جدی ریاست‌جمهوری دونالد ترامپ بود. او کمک‌های مالی بسیاری به کارزار تبلیغاتی ترامپ کرد و با برگزاری جلسات متعدد با مسئولین انتخاباتی ترامپ، در شکل‌دهی به سیاست‌های خارجی و نظامی آمریکا مشارکت داشت.

سال ۲۰۱۸، پس از تحقیق و تفحصی که درباره‌ی دخالت روسیه در نتایج انتخابات ریاست‌جمهوری آمریکا انجام شد، اطلاعات زیادی درباره لابی پرینس فاش شد که برخی از آن‌ها مربوط به ایران و ترور سردار قاسم سلیمانی بود.

این گزارش، اگرچه با حفظ مراتب امنیتی، به دست رسانه‌ها رسید و سروصدای زیادی به پا کرد. با این وجود، سال ۲۰۱۸، کسی به تأثیر پرینس در ترور شهید سلیمانی توجه نکرد؛ تا بامداد سوم ژانویه‌ی ۲۰۲۰، ‌وقتی این اتفاق به انجام رسید.

یکی از اسناد فاش شده، ایمیل اریک پرینس به «استیو بنون» مدیر وقت وب‌سایت محافظه‌کار و تندروی «بریتبارت»، بود که پرینس در آن، سردار سلیمانی و سپاه قدس را عاملان خشونت منطقه و مانع تحقق منافع آمریکا خوانده و خواستار ترور شهید سلیمانی شده بود.
این یادداشت از طریق بنون به دست «کوری لواندوسکی» مدیر کارزار تبلیغاتی ترامپ، رسید و بعدها تقدیم «دونالد ترامپ جونیور» (پسرِ ترامپ) و «مایکل فلین» مشاور امنیت ملی وقت آمریکا شد.

در این یادداشت، پرینس قاسم سلیمانی را «هاینریش هیملرِ حکومت ایران» می‌خواند و معتقد است او «بازوی» رهبر ایران است. همچنین می‌نویسد: «این‌که سلیمانی و آدم‌هایی مثل او پیشاپیش کشته نشده‌اند، یک ننگِ ملی برای آمریکاست.»

پرینس همچنین درباره‌ی ملت ایران می‌نویسد: «آن‌ها آدم‌هایی هستند که در یک اینچ مربع فرش ایرانی هزار گره می‌زنند. همین آدم‌ها روی تسلط منطقه‌ای تمرکز کرده‌اند و موفق هم بوده‌اند.» او در عین حال، کشور ایران و سپاه پاسداران را با دیکتاتوری نازی‌ها مقایسه می‌کند و معتقد است باید هرچه سریع‌تر جلوی اقدامات ایرانی‌ها را گرفت.

جالب است که توجیه اریک پرینس برای ترور سلیمانی همان است که بعداً توسط ترامپ و اطرافیانش به عنوان توجیه ترور ارائه شد. در واقع، به نظر می‌رسد پرینس به عنوان مشاور نظامی دولت ترامپ توصیه‌هایی داشته که بعدها مورد توجه دولت ترامپ قرار گرفته است.

سیاست خشونت آن‌ها از این قرار است که آن‌ها فهرستی از اقدامات نظامی منطقه را به ایران منتسب می‌کنند و در این میان، روی امنیت سفارت‌خانه‌های آمریکا در کشورهای منطقه و امنیت جانی پرسنل خود تأکید می‌کنند، تا شاید بتوانند ترور سلیمانی را موجه جلوه دهند.

بعد از ترور سلیمانی نیز رسانه‌های بین‌المللی به‌درستی به این نکته اشاره کردند که همه‌ی این مطالب در حد یک ادعا باقی می‌ماند و دولت آمریکا هیچ سند یا شاهدی برای اثبات آن‌ها و سپس توجیه ترور سلیمانی در دست ندارد.

۱۲ ژانویه‌ی ۲۰۲۰، چند روز پس از ترور سردار سلیمانی، وقتی این توصیه‌های خشونت‌طلبانه علنی شد، پرینس در مصاحبه با سی‌ان‌ان بر توصیه‌ی خود تأکید کرد و گفت: «توصیه‌ی من درباره‌ی سلیمانی در سال ۲۰۱۵ صحیح بود، همان‌طور که توصیه‌ام درباره‌ی عقلانی‌سازی شرایط [جنگ] در افغانستان صحیح بود.»

این سخنان حاکی از آن است که پرینس نه‌تنها به عنوان طراح نظریه‌ی ترور شهید سلیمانی، چهره‌ی برجسته‌ای دارد، بلکه در تشویق و ترغیب آن پس از وقوع نیز نقش داشته است.

با توجه به نفوذ نظامی پرینس در عراق و روحیه‌ی خشونت‌طلب او بعید نیست چنین شخصیتی در اجرای عملیاتیِ نقشه‌ی ترور شهید سلیمانی نیز نقشی عملی ایفا کرده باشد.
