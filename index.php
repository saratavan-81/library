<h5>درخواست شورای عالی فضای مجازی برای راه‌اندازی مرورگرهای داخلی</h5>
به گزارش زومیت، در جلسه سه‌شنبه ۲۱ تیرماه شورای عالی فضای مجازی به ریاست رئیس‌جمهور برگزار شد و پنج دستور جلسه مورد بررسی قرار گرفت. یکی از مهم‌ترین مسائل مطرح‌شده در این جلسه موضوع لزوم گسترش خدمات دیجیتالی دولت بود. همچنین اعضای این شورا با ذکر این نکته که پیام‌رسان‌های داخلی اخیراً مورد استقبال کاربران ایرانی قرار گرفته‌اند، خواستار راه‌اندازی مرورگرها و موتورهای جست‌وجوی داخلی شدند.

ادعای استقبال کاربران از پیام‌رسان‌های ایرانی در حالی توسط اعضای شورای عالی فضای مجازی مطرح شده که چند ماه قبل، مدیران عامل چند پیام‌رسان داخلی در گفت‌وگو با زومیت از رشد کند پیام‌رسان‌های ایرانی خبر داده و به چالش‌های فراوانی که در مسیر رشد و توسعه پیام‌رسان‌های داخلی وجود دارد، اشاره کرده بودند.

مدیرعامل پیام‌رسان‌ گپ تعداد کاربر فعال ماهانه این پیام‌رسان تا اردیبهشت ۱۴۰۱ را ۵ میلیون کاربر عنوان کرده بود. به گفته مدیرعامل آی‌گپ، ۴ میلیون کاربر به‌صورت ماهانه در این پیام‌رسان فعالیت دارند و تعداد کاربران فعال ماهانه سروش‌پلاس نیز ۳ میلیون اعلام شده است.

