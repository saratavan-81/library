اینترنت برای کودکان فرصت‌ها و تهدیدهای متفاوتی دارد. در همین گزارش گفته شده فرصت‌هایی مثل یادگیری و ادبیات و منابع دیجیتالی، مشارکت مدنی، خلاقیت و خودبیانی و هویت‌یابی و ارتباطات اجتماعی و همچنین تهدیدهای تجاری، خشونت‌آمیز، جنسی و ارزشی ممکن است با اتصال کودکان به اینترنت پیش رویشان باشد. زندگی کودکان امروز با فضاهای سایبری پیوند خورده و مهم پیدا کردن نقطه‌ی تعادل بین فرصت‌ها و تهدیدها است تا تعادل بین آزادی بیان و حق حریم خصوصی ایجاد شود. اما فاصله‌ی زیادی بین سیاست‌های پیشگیرانه با سیاست‌های ارتقای حقوق شهروندی دیجیتال وجود دارد: «پرورش کاربران جوان اینترنت علاوه بر وجوه سلبی و امنیتی به سیاست‌هایی نیاز دارد که توانایی، انعطاف و عزت نفس کودکان را تقویت کنند تا جایی که آن‌ها بتوانند از همه‌ی فرصت‌ها بهره گرفته و تهدیدها را به حداقل برسانند.» درحالی‌که تقریباً تمامی بسته‌های سیاستی تدوین شده از سوی نهادهای ایرانی به گفته‌ی این پژوهش، نگاهی دستوری، آمرانه و مبتنی بر چارچوب‌های از پیش تعریف شده دارند و از همه مهم‌تر از نگاه بزرگسالان تدوین شده‌اند: «حتی نهادهای اصلی دارای ارتباط با کودکان و نوجوانان همچون مدرسه و حتی خانواده در این عرصه حضور چندانی ندارند.»

اینترنت در مدارس در ایران جدی گرفته نمی‌شود و آموزش و پرورش یکی از بخش‌های منفعل دولتی در زمینه اینترنت است. سال گذشته آموزش و پرورش اعلام کرد بیش از ۳۰ درصد دانش‌آموزان به وسایل هوشمند ارتباطاتی یا اینترنت دسترسی ندارند و در دوران کرونا ۳ میلیون کودک به‌خاطر دسترسی نداشتن به سامانه‌ی شاد از تحصیل بازماندند. نگاه تربیتی و کنترل‌گرانه مسئولان حتی در این آمار هم نکته‌ی مثبت می‌بیند و زهره الهیان عضو کمیسیون امنیت ملی و سیاست خارجی مجلس گفته این کودکان درواقع از آسیب‌های مجازی مصون مانده‌اند. نگاهی که بر این سیاست‌ها حاکم است نه‌تنها کودکان و تجربه‌ها، نیازها و خواسته‌های آن‌ها را نمی‌بیند بلکه خانواده‌ها را هم در این تصمیم‌گیری‌ها مشارکت نداده است.
سیاست‌ها باید جهانی باشند

اقدامات جهانی قابل توجهی در زمینه‌ی حفاظت از کودکان در فضای مجازی انجام شده و در سندهای حقوق کودک هم می‌توان به مواردی مربوط به فضای مجازی رسید که برای رسیدن به تعادل بین حفاظت از کودکان از آسیب‌های فضای مجازی و ایجاد امکان رشد آن‌ها با کمک قابلیت‌های این فضا کمک می‌کنند. کنسوانسیون حقوق کودک سندی جهانی برای حمایت از حقوق کودکان به حساب می‌آید که ایران هم به آن پیوسته است. یزدان‌پور در پژوهش خود چهار اصل این پیمان‌نامه را در مورد محیط‌های دیجیتال هم کاربردی می‌داند: اصل عدم تبعیض، اصلی که می‌گوید منافع اصلی کودکان باید در تمامی امور تأثیرگذار بر آن‌ها مد نظر قرار داشته باشد، حق حیات، بقا و رشد و حق اظهارنظر و شنیده شدن در همه‌ی موضوعاتی که به آن‌ها ربط دارد.

یکی دیگر از فعالیت‌های جهانی که همین پژوهش هم به آن اشاره می‌کند ائتلاف‌ جهانی «ما حفاظت می‌کنیم» است که بیش از ۹۰ کشور عضو آن هستند اما ایران هنوز به آن نپیوسته است. الگوی پیشنهادی این ائتلاف برنامه‌های متنوعی چون آگاهی‌بخشی، آموزش، مشارکت کودکان و حفاظت و حمایت از قربانیان و ساز و کارهای اعمال قانون و مشارکت نهادها را در کنار هم دارد. نکته مهم این است که: «برای استفاده از مزیت‌ها و به حداقل رساندن سوءاستفاده از کودکان و استعمار ذهنی کودکان باید به چارچوب‌های هماهنگی از سیاست‌های ملی و اقدامات هدفمند با لحاظ شرایط ملی و بین‌المللی برسیم.» اینترنت یک پدیده‌ی جهانی است، فرصت‌های آن برای پیشبرد آموزش و توسعه‌ی شخصی و آسیب‌ها و تهدیدهای آن و حفاظتی که باید از کودکان صورت بگیرد هم باید جهانی دیده شود درنتیجه: «به صورت محلی و منطقه‌ای قابل مدیریت نیست و نیاز به مشارکت و همکاری با کنشگران جهانی دارد.» بسته‌های سیاستی در زمینه‌ی حفاظت از کودکان در فضای مجازی به قدری بر بومی شدن تأکید دارند که با سندهای محدود‌کننده‌ی دیگر در زمینه‌ی فضای مجازی پیوند می‌خورند. درواقع نگرانی بزرگ بسیاری این است که آیا حفاظت از کودکان بهانه‌ای برای کنترل بیشتر بر اینترنت عمومی خواهد بود؟
